// 1. Arithmetic Operators
    // +	Addition
    let x = 10, y = 5;
    console.log(x + y); 

    // -	Subtraction
    let a = 20, b = 10;
    console.log(a - b);

    // *	Multiplication 
    let c = 10, d = 5;
    console.log(c * d);

    // **	Exponentiation (ES2016)
    let e = 10, f = 3;
    console.log(e ** f);

    // /	Division
    let g = 10, h = 4;
    console.log(g / h);

    // %	Modulus (Division Remainder)
    let i = 10, j = 3;
    console.log(i % j);

    // ++	Increment
    let k = 10;
    console.log(++k); // 11
    console.log(k++); // 11

    // --	Decrement
    let l = 10;
    console.log(--l); // 9
    console.log(l--); // 9

