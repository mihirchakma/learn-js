// declaring variables using let keyword 
// and assigning values to it 
let myAge = 25;
let myName = "Mihir Chakma";

console.log("My name is " + myName + ".");
console.log("I am " + myAge + " years old.\n");


// Assignment - Assigning a new value to the variable 
myName = "Aadish Chakma";
myAge = 26;

console.log("My name is " + myName + ".");
console.log("I am " + myAge + " years old.");


// Constant - Unchangeable Variables, value cannot be changed 
const PI = 3.14;
const EMPLOYEE_ID = 1001;

console.log("PI value is " + PI + ".");
console.log("Employee ID is " + EMPLOYEE_ID + ".");

